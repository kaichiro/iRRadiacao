    private void IniciaForm(Cliente c) {
        StatusCTRL.BuscaTodos().forEach((Status sts) -> {
            cbStatus.addItem(sts);
        });

        TipoPessoaCTRL.BuscaTodos().forEach((TipoPessoa tpPess) -> {
            cbTipoPessoa.addItem(tpPess);
        });

        this.setpCliente(c);

        cbTipoPessoa.setEnabled(this.getpCliente().IsNew());

        new IconManager().SetaIcones(new JButton[]{btnGravar, btnCancelar, btnEnderecoIncluir});

        Utilitarios.RePosicionaBotoes(new JButton[]{btnContatoAdicionar, btnContatoRemoverSelecionado}, pnlContatosBotoes, 250, true);

        this.PopulaJTable_Contatos();

        this.PopulaJTable_Enderecos();
    }

    private void PreencheComponentesComObjetoCliente() {
        txtID.setText(String.valueOf(this.getpCliente().getId()));
        txtNomeFantasia_Apelido.setText(this.getpCliente().getNomeFantasia_Apelido());

        if (this.getpCliente().getStatus() != null && this.getpCliente().getTipoPessoa() != null) {
            Utilitarios.SetItemSelectedComboBox(
                    new JComboBox[]{cbStatus, cbTipoPessoa},
                    new String[]{
                        this.getpCliente().getStatus().getDescricao(),
                        this.getpCliente().getTipoPessoa() != null ? this.getpCliente().getTipoPessoa().getDescricao() : "Pessoa Juridica"
                    }
            );
        }

        if (cbTipoPessoa.getSelectedItem().toString().endsWith("sica")) {
            txtCPF.setText(this.getpCliente().getCNPJ_CPF());
            txtDtNascimento.setDate(this.getpCliente().getDataNascimento());
        } else {
            txtCNPJ.setText(this.getpCliente().getCNPJ_CPF());
        }

        txtIM.setText(this.getpCliente().getIM());
        txtIe_RG.setText(this.getpCliente().getIE_RG());
        txtRazaoSocial_Nome.setText(this.getpCliente().getRazaoSocial_Nome());
    }

    private void PreencheObjetoClienteComValoresDosComponentes() {

        if (cbTipoPessoa.getSelectedItem().toString().endsWith("sica")) {
            this.getpCliente().setCNPJ_CPF(txtCPF.getText());
            this.getpCliente().setDataNascimento(txtDtNascimento.getDate());
        } else {
            this.getpCliente().setCNPJ_CPF(txtCNPJ.getText());
        }
        this.getpCliente().setIE_RG(txtIe_RG.getText());
        this.getpCliente().setIM(txtIM.getText());
        this.getpCliente().setNomeFantasia_Apelido(txtNomeFantasia_Apelido.getText());
        this.getpCliente().setRazaoSocial_Nome(txtRazaoSocial_Nome.getText());
        this.getpCliente().setStatus((Status) cbStatus.getSelectedItem());
        this.getpCliente().setTipoPessoa((TipoPessoa) cbTipoPessoa.getSelectedItem());
        this.getpCliente().setId(Integer.valueOf(txtID.getText()));
    }

    private void PopulaJTable_Contatos() {
//        this.setpContatos(ContatoCTRL.BuscarPorCliente(this.getpCliente()));
        List<Contato> ContatosLocal = this.getpContatos();

        String[] colunas = "Id,Descri√ß√£o".split(",");

        Object[][] dados = new Object[ContatosLocal.size()][colunas.length];
        for (int linha = 0; linha < ContatosLocal.size(); linha++) {
            int coluna = 0;
            dados[linha][coluna++] = ContatosLocal.get(linha).getId();
            dados[linha][coluna++] = ContatosLocal.get(linha).getDescricao();
        }
        DefaultTableModel mdl = new DefaultTableModel(dados, colunas) {
            boolean[] canEdit = new boolean[]{
                false, true
            };

            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit[columnIndex];
            }
        };
        tblContatos.setModel(mdl);
        tblContatos.setAutoCreateRowSorter(true);
        tblContatos.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        tblContatos.setShowGrid(true);
        tblContatos.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        int xx = 0;
        tblContatos.getColumnModel().getColumn(xx++).setPreferredWidth((int) (tblContatos.getParent().getWidth() * 0.08));
        tblContatos.getColumnModel().getColumn(xx++).setPreferredWidth((int) (tblContatos.getParent().getWidth() * 0.85));
    }

    private void ContatoAdicionar() {
        this.pContatos.removeAll(this.getpContatos());

        List<Contato> ContatosLocal = new ArrayList<>();
        for (int x_ = 0; x_ < tblContatos.getRowCount(); x_++) {
            ContatosLocal.add(
                    new Contato(
                            (int) tblContatos.getValueAt(x_, 0),
                            String.valueOf(tblContatos.getValueAt(x_, 1)),
                            this.getpCliente()
                    )
            );
        }
        ContatosLocal.add(new Contato(0, "", this.getpCliente()));
        this.setpContatos(ContatosLocal);
    }

    private void PersistirContatos() {
        this.getpContatos().removeAll(this.getpContatos());
        for (int i = 0; i < tblContatos.getRowCount(); i++) {
            String ContatoDescricao_ = String.valueOf(tblContatos.getValueAt(i, 1)).concat("").trim();
            if (ContatoDescricao_.length() > 0) {
                Contato c_ = new Contato(
                        (int) tblContatos.getValueAt(i, 0),
                        String.valueOf(tblContatos.getValueAt(i, 1)).concat(""),
                        this.getpCliente()
                );
                ContatoCTRL.Gravar(c_);
            }
        }
        this.setpContatos(ContatoCTRL.BuscarPorCliente(this.getpCliente()));
        this.PopulaJTable_Contatos();
    }

    private void ContatoExcluir(int id_) {
        if (JOptionPane.showConfirmDialog(null, "Deseja realmente excluir o registro de Contato(" + id_ + ")?", "<<< Aten√ß√£o! >>>?", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_NO_OPTION) {
            ContatoCTRL.Deletar(new Contato(id_));
            this.setpContatos(ContatoCTRL.BuscarPorCliente(this.getpCliente()));
            this.PopulaJTable_Contatos();
        }
    }

