    private void cbTipoPessoaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbTipoPessoaItemStateChanged

        int eixoX = txtIM.getX();
        int eixoY = txtIe_RG.getY();
        Rectangle rectAtctive = new Rectangle(eixoX, eixoY, 200, txtIM.getHeight());
        Rectangle rectInatctive = new Rectangle(eixoX, eixoY, 0, txtIM.getHeight());

        jPanel1.remove(txtCNPJ);
        jPanel1.remove(txtCPF);

        if (cbTipoPessoa.getSelectedItem().toString().endsWith("sica")) {
            txtCPF.setEnabled(true);
            txtCNPJ.setEnabled(false);
            txtDtNascimento.setEnabled(true);
            txtCPF.setBounds(rectAtctive);
            txtCNPJ.setBounds(rectInatctive);
            jPanel1.add(txtCPF);
            this.HabilitaTXT_CNPJ_CPF(false, true);
        } else {
            txtCPF.setEnabled(false);
            txtCNPJ.setEnabled(true);
            txtDtNascimento.setEnabled(false);
            txtCPF.setBounds(rectInatctive);
            txtCNPJ.setBounds(rectAtctive);
            jPanel1.add(txtCNPJ);
            this.HabilitaTXT_CNPJ_CPF(true, false);
        }
    }//GEN-LAST:event_cbTipoPessoaItemStateChanged
