    private void IniciarForm() {
        Utilitarios.RePosicionaBotoes(new JButton[]{btnGravar, btnCancelar}, pnlAcoes, 150, true);

        StatusCTRL.BuscaTodos().forEach((Status s) -> {
            cbStatus.addItem(s);
        });

        for (Usuario.PerfilGrupo pg : Usuario.PerfilGrupo.values()) {
            cbPerfilGrupo.addItem(pg);
        }

//        txtSenha.setEnabled(this.pUsuario.IsNew());
//        txtRepetirSenha.setEnabled(this.pUsuario.IsNew());
        this.PreencheComponentesComObjeto();
    }

    private void PreencheComponentesComObjeto() {
        txtId.setText(String.valueOf(this.pUsuario.getId()));
        txtNome.setText(this.pUsuario.getNome());

        if (pUsuario.getStatus() != null) {
            Utilitarios.SetItemSelectedComboBox(new JComboBox[]{cbStatus}, new String[]{pUsuario.getStatus().toString()});
        }
        if (pUsuario.getPerfilGrupo() != null) {
            Utilitarios.SetItemSelectedComboBox(new JComboBox[]{cbPerfilGrupo}, new String[]{pUsuario.getPerfilGrupo().toString()});
        }

        txtLogin.setText(this.pUsuario.getLogin());
//        txtNovaSenha.setText(this.pUsuario.getSenha());
//        txtRepetirNovaSenha.setText(this.pUsuario.getSenha());
    }

    private void PreencheObjetoComComponentes() {
        this.pUsuario.setId(Integer.valueOf(txtId.getText()));
        this.pUsuario.setNome(txtNome.getText());
        this.pUsuario.setLogin(txtLogin.getText());
        this.pUsuario.setPerfilGrupo((Usuario.PerfilGrupo) cbPerfilGrupo.getSelectedItem());
        this.pUsuario.setStatus((Status) cbStatus.getSelectedItem());
        this.pUsuario.setSenha(String.valueOf(txtNovaSenha.getPassword()));
    }

    private void PersistirUsuario() {
        if (this.SenhaAtual == null ? txtSenhaAtual.getText() == null : this.SenhaAtual.equals(txtSenhaAtual.getText())) {
            if (txtNovaSenha.getPassword().length > 0) {
                if (txtRepetirNovaSenha.getPassword().length > 0) {
                    if (Arrays.equals(txtNovaSenha.getPassword(), txtRepetirNovaSenha.getPassword()) ){
                        this.PreencheObjetoComComponentes();
                        this.pUsuario = UsuarioCTRL.Persite(this.pUsuario);
                        this.PreencheComponentesComObjeto();
                    } else {
                        Utilitarios.MsgExcecao("Campos 'Senha' e 'Repetir Senha' não podem ser diferentes!");
                    }
                } else {
                    Utilitarios.MsgExcecao("Campo 'Repetir Senha' não podem estar sem conteúdo!");
                }
            } else {
                Utilitarios.MsgExcecao("Campo 'Senha' não pode estar sem conteúdo!");
            }
        } else {
            Utilitarios.MsgExcecao("Campo 'Senha Atual' não está igual a Senha Atual!");
        }
    }

