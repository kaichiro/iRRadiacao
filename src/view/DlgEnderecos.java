    private void IniciaForm() {
        this.setTitle(
                this.getTitle()
                        .concat(" do cliente: ")
                        .concat(this.pEndereco.getCliente().getRazaoSocial_Nome())
        );

        Utilitarios.RePosicionaBotoes(new JButton[]{btnGravar, btnFechar, btnUnidadesConsumidoras}, pnlAcao, 200, true);

        for (Enumeradors.UF value : Enumeradors.UF.values()) {
            cbUF.addItem(value);
        }

        this.PreencheCamposComOjbetoEndereco();
        this.SetaIGM();
        btnUnidadesConsumidoras.setMnemonic(KeyEvent.VK_U);
        btnProcurarEndereco.setMnemonic(KeyEvent.VK_P);
        btnBuscalIGM.setMnemonic(KeyEvent.VK_B);

    }

