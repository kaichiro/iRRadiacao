    private void btnProcurarEnderecoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcurarEnderecoActionPerformed
        DlgPesquisaEntidade dlg = new DlgPesquisaEntidade(null, true, CEPEndereco.class, null, null, true);
        dlg.setVisible(true);
        dlg.toFront();
        if (dlg.getIdSelecionado() > 0) {
            CEPEndereco cepEnder = CEPEnderecoCTRL.BuscarPorId(dlg.getIdSelecionado());
            this.pEndereco.setBairro(cepEnder.getBairro());
            this.pEndereco.setCEP(cepEnder.getCEP());
            this.pEndereco.setCidade(cepEnder.getCidade());
            this.pEndereco.setComplemento(cepEnder.getComplemento());
            this.pEndereco.setLatitude(cepEnder.getLatitude());
            this.pEndereco.setLogradouro(cepEnder.getLogradouro());
            this.pEndereco.setLongitude(cepEnder.getLongitude());
            this.pEndereco.setUF(cepEnder.getUF());

            this.PreencheCamposComOjbetoEndereco();
        }

    }//GEN-LAST:event_btnProcurarEnderecoActionPerformed

