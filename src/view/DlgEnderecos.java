    private javax.swing.JFormattedTextField txtCEP;
    private javax.swing.JFormattedTextField txtCidade;
    private javax.swing.JFormattedTextField txtComplemento;
    private javax.swing.JFormattedTextField txtId;
    private javax.swing.JFormattedTextField txtLatitude;
    private javax.swing.JFormattedTextField txtLogradouro;
    private javax.swing.JFormattedTextField txtLongitude;
    private javax.swing.JFormattedTextField txtNumero;
    // End of variables declaration//GEN-END:variables

    private void PreencheCamposComOjbetoEndereco() {
        txtId.setText(String.valueOf(this.pEndereco.getId()));
        if (this.pEndereco.getBairro() != null) {
            if (this.pEndereco.getBairro().trim().length() != 0) {
                txtBairro.setText(this.pEndereco.getBairro());
            }
        }
        txtCEP.setText(this.pEndereco.getCEP());
        txtCidade.setText(this.pEndereco.getCidade());
        if (this.pEndereco.getComplemento() != null) {
            if (this.pEndereco.getComplemento().trim().length() != 0) {
                txtComplemento.setText(this.pEndereco.getComplemento());
            }
        }
        txtLatitude.setText(String.valueOf(this.pEndereco.getLatitude()).replace(",", "").replace(".", ","));
        if (this.pEndereco.getLogradouro() != null) {
            if (this.pEndereco.getLogradouro().trim().length() != 0) {
                txtLogradouro.setText(this.pEndereco.getLogradouro());
            }
        }
        txtLongitude.setText(String.valueOf(this.pEndereco.getLongitude()).replace(",", "").replace(".", ","));
        txtNumero.setText(this.pEndereco.getNumero());
        cbUF.setSelectedItem(this.pEndereco.getUF());
    }

    private void PreencheObjetoEnderecoComCampos() {
        this.pEndereco.setBairro(txtBairro.getText());
        this.pEndereco.setCEP(txtCEP.getText());
        this.pEndereco.setCidade(txtCidade.getText());
        this.pEndereco.setComplemento(txtComplemento.getText());
        this.pEndereco.setId(Integer.valueOf(txtId.getText()));
        this.pEndereco.setLatitude(Utilitarios.ConverteTextoEmDouble(txtLatitude.getText()));
        this.pEndereco.setLongitude(Utilitarios.ConverteTextoEmDouble(txtLongitude.getText()));
        this.pEndereco.setLogradouro(txtLogradouro.getText());
        this.pEndereco.setNumero(txtNumero.getText());
        this.pEndereco.setUF((Enumeradors.UF) cbUF.getSelectedItem());
    }

    private void SetaIGM() {
        this.pEndereco.setIrradiacaoGlobalMes(IrradiacaoGlobalMesCTRL.BuscarPorCoordenadas(this.pEndereco.getLatitude(), this.pEndereco.getLongitude()));
        mmIGM.setText(this.pEndereco.getIrradiacaoGlobalMes().asString());
    }
