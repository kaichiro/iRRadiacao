    private void PopulaJTableCliente() {
        String[] colunas = new String[]{"Id", "Nome", "Tipo", "CNPJ/CPF", "Status"};
        int QtdeRegistros = 0;
        this.ClienteSelecionado = null;
        this.ClientesListaPesquisa.removeAll(this.ClientesListaPesquisa);
        if (!FirstRun) {
            if (txtConteudoPesquisaCliente.getText().trim().length() > 0) {
//                System.out.println(new Date());
                this.setClientesListaPesquisa(ClienteCTRL.BuscarPorNome((int) spnrQtdeMaxCliente.getValue(), txtConteudoPesquisaCliente.getText(), null));
//                System.out.println(new Date());
                QtdeRegistros = this.ClientesListaPesquisa.size();
            } else {
                JOptionPane.showMessageDialog(null, "Digite alguma informação para a pesquisa!", "<<<ATENÇÃO>>>", 2);
            }
        } else {
            this.ClienteSelecionado = new Cliente();
        }

        Object[][] dados = new Object[QtdeRegistros][colunas.length];

        for (int linha = 0; linha < QtdeRegistros; linha++) {
            int coluna = 0;
            dados[linha][coluna++] = this.ClientesListaPesquisa.get(linha).getId();
            dados[linha][coluna++] = this.ClientesListaPesquisa.get(linha).getRazaoSocial_Nome();
            dados[linha][coluna++] = this.ClientesListaPesquisa.get(linha).getTipoPessoa();
            dados[linha][coluna++] = this.ClientesListaPesquisa.get(linha).getCNPJ_CPF();
            dados[linha][coluna++] = this.ClientesListaPesquisa.get(linha).getStatus();
        }

        DefaultTableModel mdl = new DefaultTableModel(dados, colunas) {
            boolean[] canEdit = new boolean[]{
                false, false, false, false, false
            };

            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit[columnIndex];
            }
        };
        tblClientes.setModel(mdl);
        tblClientes.setAutoCreateRowSorter(true);
        tblClientes.setAutoResizeMode(JTable.AUTO_RESIZE_ALL_COLUMNS);
        tblClientes.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        tblClientes.setShowGrid(true);
        tblClientes.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        tblClientes.getColumnModel().getColumn(0).setPreferredWidth(50);
        tblClientes.getColumnModel().getColumn(1).setPreferredWidth(tblClientes.getWidth() - 50 - 100 - 130 - 50);
        tblClientes.getColumnModel().getColumn(2).setPreferredWidth(100);
        tblClientes.getColumnModel().getColumn(3).setPreferredWidth(130);
        tblClientes.getColumnModel().getColumn(4).setPreferredWidth(50);
    }

