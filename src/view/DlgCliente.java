    public void setClienteSelecionado(/*Cliente ClienteSelecionado*/) {
        for (int i = 0; i < this.ClientesListaPesquisa.size(); i++) {
            if (this.ClientesListaPesquisa.get(i).getId() == (int) tblClientes.getValueAt(tblClientes.getSelectedRow(), 0)) {
                this.ClienteSelecionado = this.ClientesListaPesquisa.get(i);
                break;
            }
        }

        this.ClienteSelecionado.setEnderecos(EnderecoCTRL.BuscarPorCliente(ClienteSelecionado));
        this.ClienteSelecionado.setContatos(ContatoCTRL.BuscarPorCliente(ClienteSelecionado));

        this.PopulaComponentesCliente();

        this.PopulaJTableEndereco();
        this.PopulaJTableContato();

    }

